public class AdInfo {

    private String mediaId;
    private String mediaType;
    private String areaAttr;
    private String mediaAddr;
    private String adContent;
    private String direct;
    private String adimgurl;
    private double longitude;
    private double latitude;

    public String getMediaId() {
        return mediaId;
    }

    public void setMediaId(String mediaId) {
        this.mediaId = mediaId;
    }

    public String getMediaType() {
        return mediaType;
    }

    public void setMediaType(String mediaType) {
        this.mediaType = mediaType;
    }

    public String getAreaAttr() {
        return areaAttr;
    }

    public void setAreaAttr(String areaAttr) {
        this.areaAttr = areaAttr;
    }

    public String getMediaAddr() {
        return mediaAddr;
    }

    public void setMediaAddr(String mediaAddr) {
        this.mediaAddr = mediaAddr;
    }

    public String getAdContent() {
        return adContent;
    }

    public void setAdContent(String adContent) {
        this.adContent = adContent;
    }

    public String getDirect() {
        return direct;
    }

    public void setDirect(String direct) {
        this.direct = direct;
    }

    public String getAdimgurl() {
        return adimgurl;
    }

    public void setAdimgurl(String adimgurl) {
        this.adimgurl = adimgurl;
    }

    public double getLongitude() {
        return longitude;
    }

    public void setLongitude(double longitude) {
        this.longitude = longitude;
    }

    public double getLatitude() {
        return latitude;
    }

    public void setLatitude(double latitude) {
        this.latitude = latitude;
    }
}
